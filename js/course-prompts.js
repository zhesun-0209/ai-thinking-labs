function stripHtml(text) {
  return String(text)
    .replace(/<[^>]+>/g, "")
    .replace(/\*\*/g, "")
    .replace(/\s+\n/g, "\n")
    .trim();
}

/** 直接返回 cell 上写好的 copyPrompt；没有则退回页面 prompt 的纯文本 */
function buildCopyPrompt(cell) {
  const raw = (cell.copyPrompt || cell.prompt || "").trim();
  return raw ? stripHtml(raw) : "";
}


